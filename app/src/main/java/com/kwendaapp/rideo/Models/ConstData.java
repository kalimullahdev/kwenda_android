package com.kwendaapp.rideo.Models;

public class ConstData {
    private final String key;
    private final String value;

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

    public ConstData(String key, String value) {
        this.key = key;
        this.value = value;
    }
}
